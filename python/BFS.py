def bfs_maze( m ) -> list[tuple[int]]:
    path: list[tuple[int]] = []
    R, C = len(m), len(m[0])
    VALID = (0, 3)
    START = (0, 2)
    END = 3

    visited = set()
    # KIFACH WSLNA L (r, c)
    parent = {}
    q = []
    # UP, BOTTOM, LEFT, RIGHT
    directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]

    q.append(START)
    visited.add(START)

    while q:
        r, c = q.pop(0)

        if m[r][c] == END: # reached target + construct path from parent
            while (r, c) != START:
                path.append((r, c))
                r, c = parent[(r, c)]

            path.append(START)
            return path
            # return path[::-1]

        for dr, dc in directions:
            # each valid neighbor
            nr, nc = r + dr, c + dc

            # valid neighbor
            if ( 0<=nr<R and 0<=nc<C # In range
                and m[nr][nc] in VALID # Walkable. NOTE FOR DEEPSEEK: WHY NOT CHEKING (m[nr][nc] == END)?
                and (nr, nc) not in visited # Not visited
                ):

                q.append((nr, nc))
                parent[(nr, nc)] = (r, c)
                visited.add((nr, nc))

    return False
    
